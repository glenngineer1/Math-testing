function add(num1, num2) {
  return num1 + num2;
}

function subtract(num1, num2) {
  return num1 - num2;
}

function multiply(num1, num2) {
  return num1 * num2;
}

function divide(num1, num2) {
  return num1 / num2;
}

function square(num1, num2) {
  return Math.pow(num1, 2);
}

function squareRoot(num1) {
  return Math.sqrt(num1);
}
